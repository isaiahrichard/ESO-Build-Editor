def write_versions(f, version):
    f.write("-- ------------------------------------------------------------\n")
    f.write("-- Versions\n")
    f.write("-- ------------------------------------------------------------\n\n")
    f.write(f"INSERT INTO `versions` (`version`) VALUES ({q(version)});\n\n")


def write_computed_stats(f, stats_data):
    f.write("-- ------------------------------------------------------------\n")
    f.write("-- computedStats\n")
    f.write("-- ------------------------------------------------------------\n\n")

    rows = []
    for stat_id, stat in stats_data.items():
        if not isinstance(stat, dict):
            continue
        compute_val = stat.get("compute", "[]")
        if isinstance(compute_val, list):
            compute_val = json.dumps(compute_val)
        depends_on = stat.get("dependsOn", "")
        if isinstance(depends_on, list):
            depends_on = json.dumps(depends_on)
        rows.append(
            f"  ({n(stat.get('id'))}, {q(stat.get('statId',''))}, {q(stat.get('version',''))}, "
            f"{q(stat.get('title',''))}, {q(stat.get('roundNum', stat.get('round','')))}, "
            f"{q(stat.get('addClass',''))}, {q(stat.get('comment',''))}, "
            f"{n(stat.get('minimumValue'))}, {n(stat.get('maximumValue'))}, "
            f"{n(stat.get('deferLevel'))}, {q(stat.get('display',''))}, "
            f"{q(compute_val)}, {n(stat.get('idx',0))}, "
            f"{q(stat.get('category',''))}, {q(stat.get('suffix',''))}, "
            f"{q(depends_on)})"
        )

    if rows:
        f.write("INSERT INTO `computedStats` "
                "(`id`,`statId`,`version`,`title`,`roundNum`,`addClass`,`comment`,"
                "`minimumValue`,`maximumValue`,`deferLevel`,`display`,`compute`,"
                "`idx`,`category`,`suffix`,`dependsOn`) VALUES\n")
        f.write(",\n".join(rows))
        f.write(";\n\n")


def write_rules_and_effects(f, rules_data):
    f.write("-- ------------------------------------------------------------\n")
    f.write("-- Rules\n")
    f.write("-- ------------------------------------------------------------\n\n")

    rule_rows = []
    effect_rows = []

    for rule_type, rules_by_id in rules_data.items():
        if rule_type not in RULE_TYPES:
            continue
        if not isinstance(rules_by_id, dict):
            continue
        for rule_id, rule in rules_by_id.items():
            if not isinstance(rule, dict):
                continue
            rule_rows.append(
                f"  ({n(rule.get('id'))}, {q(rule.get('version',''))}, "
                f"{q(rule.get('ruleType',''))}, {q(rule.get('nameId'))}, "
                f"{q(rule.get('displayName'))}, {q(rule.get('matchRegex',''))}, "
                f"{q(rule.get('displayRegex'))}, {q(rule.get('statRequireId',''))}, "
                f"{q(rule.get('statRequireValue',''))}, {q(rule.get('factorStatId',''))}, "
                f"{b(rule.get('isEnabled'))}, {b(rule.get('isVisible'))}, "
                f"{b(rule.get('isToggle'))}, {b(rule.get('enableOffBar'))}, "
                f"{q(rule.get('originalId',''))}, {q(rule.get('icon',''))}, "
                f"{q(rule.get('groupName',''))}, {n(rule.get('maxTimes'))}, "
                f"{q(rule.get('comment',''))}, {q(rule.get('description',''))}, "
                f"{q(rule.get('customData','{}'))})"
            )
            for effect in rule.get("effects", []):
                effect_rows.append(
                    f"  ({n(effect.get('id') or effect.get('effectId'))}, "
                    f"{n(effect.get('ruleId'))}, {q(effect.get('version',''))}, "
                    f"{q(effect.get('statId',''))}, {q(effect.get('value',''))}, "
                    f"{q(effect.get('display',''))}, {q(effect.get('category',''))}, "
                    f"{q(effect.get('combineAs',''))}, "
                    f"{q(effect.get('roundNum', effect.get('round','')))}, "
                    f"{n(effect.get('factorValue'))}, {q(effect.get('statDesc',''))}, "
                    f"{q(effect.get('buffId',''))}, {q(effect.get('regexVar',''))})"
                )

    chunk_size = 500

    for i in range(0, len(rule_rows), chunk_size):
        chunk = rule_rows[i:i + chunk_size]
        f.write("INSERT INTO `rules` "
                "(`id`,`version`,`ruleType`,`nameId`,`displayName`,`matchRegex`,"
                "`displayRegex`,`statRequireId`,`statRequireValue`,`factorStatId`,"
                "`isEnabled`,`isVisible`,`isToggle`,`enableOffBar`,`originalId`,"
                "`icon`,`groupName`,`maxTimes`,`comment`,`description`,`customData`) VALUES\n")
        f.write(",\n".join(chunk))
        f.write(";\n\n")

    f.write("-- ------------------------------------------------------------\n")
    f.write("-- Effects\n")
    f.write("-- ------------------------------------------------------------\n\n")

    for i in range(0, len(effect_rows), chunk_size):
        chunk = effect_rows[i:i + chunk_size]
        f.write("INSERT INTO `effects` "
                "(`id`,`ruleId`,`version`,`statId`,`value`,`display`,`category`,"
                "`combineAs`,`roundNum`,`factorValue`,`statDesc`,`buffId`,`regexVar`) VALUES\n")
        f.write(",\n".join(chunk))
        f.write(";\n\n")


def main():
    print("Reading rules.json ...")
    with open(RULES_FILE, "r", encoding="utf-8") as fh:
        rules_data = json.load(fh)

    # The 'stats' key in rules.json is the raw DB format for computedStats
    stats_data = rules_data.get("stats", {})

    print(f"  Rule types found: {[k for k in rules_data if k in RULE_TYPES]}")
    print(f"  computedStats entries: {len(stats_data)}")

    total_rules = sum(
        len(v) for k, v in rules_data.items()
        if k in RULE_TYPES and isinstance(v, dict)
    )
    total_effects = sum(
        len(rule.get("effects", []))
        for k, v in rules_data.items()
        if k in RULE_TYPES and isinstance(v, dict)
        for rule in v.values()
        if isinstance(rule, dict)
    )
    print(f"  Rules: {total_rules}, Effects: {total_effects}")

    print(f"Writing {OUTPUT_FILE} ...")
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        write_schema(f)
        version = next(
            (r.get("version") for rtype in rules_data.values()
             if isinstance(rtype, dict)
             for r in rtype.values()
             if isinstance(r, dict) and r.get("version")),
            "49"
        )
        write_versions(f, version)
        write_computed_stats(f, stats_data)
        write_rules_and_effects(f, rules_data)
        f.write("SET foreign_key_checks = 1;\n")

    size_mb = os.path.getsize(OUTPUT_FILE) / (1024 * 1024)
    print(f"Done. seed.sql is {size_mb:.1f} MB")


if __name__ == "__main__":
    main()
