"""
Converts the exported rules.json and computedStats.json from the UESP build editor
into a MySQL seed SQL file for local development.

Usage: python generate_seed_sql.py
Output: seed.sql
"""

import json
import os

RULES_FILE = os.path.join(os.path.dirname(__file__), "rules.json")
COMPUTED_STATS_FILE = os.path.join(os.path.dirname(__file__), "computedStats.json")
OUTPUT_FILE = os.path.join(os.path.dirname(__file__), "seed.sql")

RULE_TYPES = {
    "buff", "set", "cp", "passive", "mundus", "active",
    "armorenchant", "weaponenchant", "offhandweaponenchant", "abilitydesc",
}


def q(value):
    """Escape and quote a string value for SQL. Returns NULL for None."""
    if value is None:
        return "NULL"
    return "'" + str(value).replace("\\", "\\\\").replace("'", "\\'") + "'"


def n(value):
    """Return NULL for None, otherwise a numeric literal."""
    if value is None:
        return "NULL"
    return str(value)


def b(value):
    """Convert bool/truthy to SQL 0 or 1."""
    return "1" if value else "0"


